// Function Syntax

// Function Definition
const avg = function(a, b) {
  return (a + b) / 2;
}

// Function call
console.log(avg(5, 6));

// another Definition
const print = function(a,b) {
  console.log(`a=${a}, b=${b}`)
};

// Undefined values
const result = print(7, 8);
console.log(result);
print(9);
print();
